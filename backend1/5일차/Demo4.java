package java_5day;

public class Demo4 {

	public static void main(String[] args) {
		int number =3;
		switch (number) {
		case 3:
			System.out.println("3을선택했군요");
			break;
		case 2:
			System.out.println("2을선택했군요");
			break;
		case 1:
			System.out.println("1을선택했군요");
			break;
		default:
			System.out.println("1 2 3 이 아닌 값을 선택했군요");
		}

	}

}
