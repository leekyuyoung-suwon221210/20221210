package backend1;

public class Sample2 {

	public static void main(String[] args) {
		// 변수만드는 방법
		// 변수타입 변수명 = 데이터;
		// 정수형 변수 만들기
		//  = 이 기호는 대입연산이며 오른쪽에서 왼쪽으로 값을 대입한다.		
		int number = 10;
		System.out.println( number );		
		// 만들어진 변수는 아래와같이 쓴다.
		number = 200;
		System.out.println(number);

	}

}
