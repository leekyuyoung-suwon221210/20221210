package operator;

public class Demo2 {

	public static void main(String[] args) {
		// 정수형 변수 만들고 10으로 초기화
		int num = 10;
		// 관계(비교) 연산자  true false
		System.out.println(num > 3);
		System.out.println(num < 3);
		System.out.println(num >= 3);
		System.out.println(num <= 3);
		System.out.println(num == 3);
		System.out.println(num != 3);
		System.out.println(num = 3);
		
		boolean temp = false;
		temp = num > 3;
		System.out.println(temp);
		temp = num < 3;
		System.out.println(temp);
		
		
		
		
	}

}
